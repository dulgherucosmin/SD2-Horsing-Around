    private void letPlayerLand(Player p) {
        // keep updating player until they fall on the ground prior to testing
        for (int i = 0; i < 200; i++) {
            p.update();
        }
    }

    @Test
    public void testVoidTeleport() {

        Player p = new Player(1, 40, 1, null, RIGHT, "Test", null);
        // load our test level into player code
        p.loadLevelData(createVoidLevel());
        // set current level to ensure hitboxes work
        p.setCurentLevel(3);

        // let player land before testing begins
        letPlayerLand(p);

        // move right
        p.setRight(true);
        p.update();

        // make player move until they reach void
        for (int i = 0; i < 37; i++) {
            p.update();
        }

        // player spawned then moved 1 time (so spawn pos + 1 movement tick)
        float expectedPosition = getSpawnPoint(1, 3)[0] + p.getPlayerSpeed();

        // check if player has been reset back to spawn
        assertEquals(p.getX(), expectedPosition, 0);
    }

